// ============================================================================
// 类型定义
// ============================================================================

export interface MemberPreference {
  userId: string;
  mood: string;
  difficulty: string;
  condition: string;
  availableTime: number;
  maxDistance: number;
}

export interface SynthesizedPreferences {
  synthesizedMood: string;
  synthesizedDifficulty: string;
  synthesizedCondition: string;
  analysis: string;
  considerations: string[];
}

export interface GroupNegotiationResult {
  teamId: string;
  memberCount: number;
  synthesizedPreferences: SynthesizedPreferences;
  recommendedRoutes: any[]; // RouteMatchScore[]
  autoGeneratedTitle: string;
  negotiationId: string;
}

// ============================================================================
// 主流程：Social Negotiation
// ============================================================================

/**
 * 执行团队的 "Social Negotiation" - AI 综合所有队员的偏好
 * @param teamId - 团队 ID
 * @param initiatorId - 启动者 ID（应该是队长）
 * @returns 综合分析结果和推荐路由
 */
export async function performGroupNegotiation(
  teamId: string,
  initiatorId: string
): Promise<GroupNegotiationResult> {
  console.log(`[Group Negotiation] Starting for team ${teamId}`);

  // 1. 验证队长身份
  const isLeader = await teamService.isTeamLeader(teamId, initiatorId);
  if (!isLeader) {
    throw new Error('Only team leader can initiate group negotiation');
  }

  // 2. 检查所有成员是否完成了偏好设置
  const allCompleted = await teamService.areAllPreferencesCompleted(teamId);
  if (!allCompleted) {
    throw new Error('Not all team members have completed their preferences');
  }

  // 3. 获取所有队员的偏好
  const memberPreferences = await teamService.getAllMemberPreferences(teamId);
  if (memberPreferences.length === 0) {
    throw new Error('No member preferences found');
  }

  console.log(`[Group Negotiation] Retrieved ${memberPreferences.length} member preferences`);

  // 4. 调用 AI 进行综合分析
  const synthesized = await synthesizePreferencesWithAI(memberPreferences);

  console.log('[Group Negotiation] AI analysis completed');
  console.log('Synthesized mood:', synthesized.synthesizedMood);
  console.log('Synthesized difficulty:', synthesized.synthesizedDifficulty);
  console.log('Synthesized condition:', synthesized.synthesizedCondition);

  // 5. 基于综合偏好进行路由匹配
  const recommendedRoutes = await findMatchingRoutes({
    mood: synthesized.synthesizedMood,
    difficulty: synthesized.synthesizedDifficulty,
    condition: synthesized.synthesizedCondition,
    availableTime: calculateAverageTime(memberPreferences),
    maxDistance: calculateAverageDistance(memberPreferences),
  });

  console.log(`[Group Negotiation] Found ${recommendedRoutes.length} recommended routes`);

  // 6. 自动生成分组标题
  const autoTitle = generateGroupTitle(synthesized.synthesizedMood, memberPreferences.length);

  // 7. 保存到数据库
  const negotiationId = await saveNegotiationHistory(
    teamId,
    initiatorId,
    memberPreferences,
    synthesized,
    recommendedRoutes
  );

  // 8. 更新团队的综合偏好
  await updateTeamSynthesizedPreferences(teamId, synthesized);

  return {
    teamId,
    memberCount: memberPreferences.length,
    synthesizedPreferences: synthesized,
    recommendedRoutes,
    autoGeneratedTitle: autoTitle,
    negotiationId,
  };
}

// ============================================================================
// AI 综合分析
