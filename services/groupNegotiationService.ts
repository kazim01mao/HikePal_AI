/**
 * 使用 Gemini AI 综合所有队员的偏好
 * @param preferences - 所有队员的偏好数组
 * @returns 综合后的偏好和分析
 */
async function synthesizePreferencesWithAI(
  preferences: MemberPreference[]
): Promise<SynthesizedPreferences> {
  // 构建提示词
  const prompt = constructNegotiationPrompt(preferences);

  console.log('[AI Negotiation] Sending prompt to Gemini API');
  console.log('Prompt:', prompt);

  try {
    const response = await callGeminiAPI(prompt);

    // 解析 AI 响应
    const parsed = parseAIResponse(response);

    return parsed;
  } catch (error) {
    console.error('[AI Negotiation] Error calling Gemini API:', error);
    throw error;
  }
}

/**
 * 构建 AI 提示词 - 指导 AI 如何综合队员偏好
 */
function constructNegotiationPrompt(preferences: MemberPreference[]): string {
  const memberSummaries = preferences
    .map((p, idx) => {
      return `
Member ${idx + 1}:
- Mood/Style: ${p.mood}
- Difficulty Level: ${p.difficulty} (1-5 scale)
- Specific Requests: ${p.condition}
- Available Time: ${p.availableTime} minutes
- Maximum Distance: ${p.maxDistance} km
`;
    })
    .join('\n');

  const prompt = `You are an expert hiking tour guide and group coordinator. Your task is to synthesize the hiking preferences of a group and create a unified hiking plan that satisfies everyone.

Group Members' Preferences:
${memberSummaries}

Based on all members' preferences, please:

1. Analyze the group's combined interests and constraints
2. Identify common themes and potential compromises
3. Determine a synthesized hiking approach that balances all preferences
4. Provide the output in the following JSON format:

{
  "synthesizedMood": "[one of: Adventurous, Peaceful, Scenic, Social, Challenging, Family-friendly, or a combination]",
  "synthesizedDifficulty": "[easy, medium, or hard]",
  "synthesizedCondition": "[A 1-2 sentence description of the ideal hiking route for this group, considering all constraints]",
  "analysis": "[2-3 sentences explaining your recommendations and how they address the group's needs]",
  "considerations": [
    "[Important consideration 1]",
    "[Important consideration 2]",
    "[Important consideration 3]"
  ]
}

IMPORTANT:
- The synthesizedMood should reflect the group's overall character
- Difficulty should be achievable for all members (match the lowest requirement or one level above)
- The synthesizedCondition should be specific and actionable for route matching
- Ensure your analysis is balanced and considers all preferences equally
- Return ONLY valid JSON, no additional text`;

  return prompt;
}

/**
 * 解析 AI 返回的响应
 */
function parseAIResponse(response: string): SynthesizedPreferences {
  console.log('[AI Response] Parsing response');

  // 尝试提取 JSON
  const jsonMatch = response.match(/\{[\s\S]*\}/);
  if (!jsonMatch) {
    console.error('[AI Response] No JSON found in response');
    throw new Error('AI response does not contain valid JSON');
  }

  const jsonStr = jsonMatch[0];
  console.log('[AI Response] Extracted JSON:', jsonStr);

  try {
    const parsed = JSON.parse(jsonStr);

    // 验证必需字段
    if (
      !parsed.synthesizedMood ||
      !parsed.synthesizedDifficulty ||
      !parsed.synthesizedCondition
    ) {
      throw new Error('Missing required fields in AI response');
    }

    return {
      synthesizedMood: parsed.synthesizedMood,
      synthesizedDifficulty: parsed.synthesizedDifficulty,
      synthesizedCondition: parsed.synthesizedCondition,
      analysis: parsed.analysis || '',
      considerations: parsed.considerations || [],
    };
  } catch (error) {
    console.error('[AI Response] JSON parse error:', error);
    throw error;
  }
}

