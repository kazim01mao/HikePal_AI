/**
 * 计算队员的平均可用时间
 */
function calculateAverageTime(preferences: MemberPreference[]): number {
  if (preferences.length === 0) return 300;

  const times = preferences.map((p) => p.availableTime);
  const sum = times.reduce((a, b) => a + b, 0);

  // 返回最小时间和平均时间中较小的值（保守估计）
  const min = Math.min(...times);
  const avg = Math.round(sum / times.length);

  return Math.min(min, avg);
}

/**
 * 计算队员的平均最大距离（保守估计）
 */
function calculateAverageDistance(preferences: MemberPreference[]): number {
  if (preferences.length === 0) return 20;

  const distances = preferences.map((p) => p.maxDistance);

  // 返回最小距离（因为这是所有人都能接受的）
  return Math.min(...distances);
}

/**
 * 自动生成分组标题
 */
function generateGroupTitle(mood: string, memberCount: number): string {
  const moodTitles: Record<string, string> = {
    adventurous: 'Adventure Squad Challenge',
    peaceful: 'Serenity Seekers Hike',
    scenic: 'Scenic Explorers Route',
    social: 'Fellowship Trail',
    challenging: 'Peak Hunters Journey',
    'family-friendly': 'Family Adventure Day',
    mixed: 'Group Harmony Trail',
  };

  const baseMood = mood.toLowerCase().split(' ')[0];
  const baseTitle = moodTitles[baseMood] || 'Group Harmony Trail';

  return `${baseTitle} (${memberCount} members)`;
}

