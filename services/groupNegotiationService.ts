/**
 * 保存协商历史到数据库
 */
async function saveNegotiationHistory(
  teamId: string,
  initiatorId: string,
  memberPreferences: MemberPreference[],
  synthesized: SynthesizedPreferences,
  recommendedRoutes: any[]
): Promise<string> {
  const { data, error } = await supabase
    .from('team_negotiation_history')
    .insert({
      team_id: teamId,
      initiated_by: initiatorId,
      member_preferences: memberPreferences,
      member_count: memberPreferences.length,
      ai_analysis: synthesized.analysis,
      synthesized_mood: synthesized.synthesizedMood,
      synthesized_difficulty: synthesized.synthesizedDifficulty,
      synthesized_condition: synthesized.synthesizedCondition,
      recommended_routes: recommendedRoutes.map((r) => ({
        id: r.id,
        name: r.name,
        score: r.score,
        matchReasons: r.matchReasons,
      })),
    })
    .select('id')
    .single();

  if (error) {
    console.error('[DB] Error saving negotiation history:', error);
    throw error;
  }

  return data.id;
}

/**
 * 保存生成的路由和标题
 */
async function saveGeneratedRoutes(
  teamId: string,
  negotiationId: string,
  generatedBy: string,
  synthesized: SynthesizedPreferences,
  recommendedRoutes: any[],
  autoTitle: string
): Promise<string> {
  const { data, error } = await supabase
    .from('team_generated_routes')
    .insert({
      team_id: teamId,
      negotiation_id: negotiationId,
      generated_by: generatedBy,
      synthetic_query: synthesized.synthesizedCondition,
      routes: recommendedRoutes,
      auto_generated_title: autoTitle,
    })
    .select('id')
    .single();

  if (error) {
    console.error('[DB] Error saving generated routes:', error);
    throw error;
  }

  return data.id;
}

/**
 * 更新团队的综合偏好
 */
async function updateTeamSynthesizedPreferences(
  teamId: string,
  synthesized: SynthesizedPreferences
): Promise<void> {
  const { error } = await supabase
    .from('teams')
    .update({
      hiking_mood: synthesized.synthesizedMood,
      hiking_difficulty: synthesized.synthesizedDifficulty,
      hiking_preferences: {
        synthesized_condition: synthesized.synthesizedCondition,
        considerations: synthesized.considerations,
      },
      negotiation_completed: true,
      updated_at: new Date().toISOString(),
    })
    .eq('id', teamId);

  if (error) {
    console.error('[DB] Error updating team preferences:', error);
    throw error;
  }
}

/**
 * 选择推荐的路由
 */
export async function selectGroupRoute(
  teamId: string,
  routeId: string,
  negotiationId: string
): Promise<void> {
  const { error } = await supabase
    .from('team_negotiation_history')
    .update({
      selected_route_id: routeId,
    })
    .eq('id', negotiationId);

  if (error) {
    console.error('[DB] Error selecting route:', error);
    throw error;
  }

  // 更新团队状态
  await supabase
    .from('teams')
    .update({
      status: 'hiking',
      updated_at: new Date().toISOString(),
    })
    .eq('id', teamId);
}

/**
 * 获取团队的协商历史
 */
export async function getTeamNegotiationHistory(teamId: string): Promise<any[]> {
  const { data, error } = await supabase
    .from('team_negotiation_history')
    .select('*')
    .eq('team_id', teamId)
    .order('created_at', { ascending: false });

  if (error) {
    console.error('[DB] Error fetching negotiation history:', error);
    throw error;
  }

  return data || [];
}

/**
 * 重新运行协商（如果有成员更改了偏好）
 */
export async function rerunNegotiation(
  teamId: string,
  initiatorId: string
): Promise<GroupNegotiationResult> {
  console.log('[Negotiation] Rerunning negotiation for team:', teamId);
  return performGroupNegotiation(teamId, initiatorId);
}

